//8. Write a program in java to show the foreach
//   version of for loop to iterate through array.
package Lab1;

public class QN8 {
    public static void main(String[] args) {
        int[] arr = {1, 2, 3, 4, 5};
        for (int i : arr) {
            System.out.println(i);
        }
    }
}
